<script>
    document.addEventListener('alpine:init', () => {
        Alpine.data('customerStories', () => ({
            currentStep: 0,
            totalSteps: 4,
            canGoBack: false,
            canGoForward: true,
            setStep: function(step) {
                this.currentStep = step;
                this.canGoForward = step < this.totalSteps - 1;
                this.canGoBack = step > 0;
            },
            previousStep() {
                if (this.currentStep > 0) this.setStep(this.currentStep - 1);
            },
            nextStep() {
                if (this.currentStep < this.totalSteps - 1) this.setStep(this.currentStep + 1);
            }
        }));
    });
